var canvas = document.getElementById('canvas-line-team');
if (canvas.getContext) {
    var ctx = canvas.getContext('2d');
    // Line
    ctx.fillRect(240, 30, 8, 150);
    // Circle
    var circle = canvas.getContext("2d");
    circle.beginPath();
    circle.arc(244, 18, 12, 0, 2 * Math.PI);
    circle.stroke();
    // Nav
    ctx.beginPath();
    ctx.moveTo(280, 20);
    ctx.lineTo(300, 35);
    ctx.lineTo(300, 7);
    ctx.fill();
}
